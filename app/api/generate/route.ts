async function analyzeViralPotential(content: string, platform: string) {
  try {
    const analysisPrompt = `Ты — эксперт по вирусному контенту. Проанализируй этот контент для ${platform} и верни JSON.

Контент:
${content.slice(0, 800)}

Верни ТОЛЬКО валидный JSON без пояснений:
{"score":NUMBER_80_97,"positives":["конкретный плюс 1","конкретный плюс 2","конкретный плюс 3"],"improvements":["что улучшить 1","что улучшить 2"]}

Правила:
- score: реальная оценка 80-97, учитывай хук, CTA, цифры, платформу
- positives: конкретные сильные стороны этого контента (упоминай цифры если есть)
- improvements: конкретные рекомендации по улучшению`;

    const msg = await anthropic.messages.create({
      model: "claude-sonnet-4-6",
      max_tokens: 300,
      messages: [{ role: "user", content: analysisPrompt }],
    });

    const raw = msg.content[0].type === "text" ? msg.content[0].text.trim() : "";
    const jsonMatch = raw.match(/\{[\s\S]*\}/);
    if (jsonMatch) return JSON.parse(jsonMatch[0]);
  } catch {}
  return null;
}

export async function POST(req: NextRequest) {
  try {
    const { type, topic, platform, tone, goal, brandVoice, direction, directionParams } = await req.json();

    if (!topic || !platform) {
      return NextResponse.json({ error: "Не указаны обязательные параметры" }, { status: 400 });
    }

    let prompt = direction
      ? buildDirectionPrompt(direction, topic, platform, tone || "Доверительный", directionParams || {})
      : buildPrompt(type || "scenario", topic, platform, tone || "Доверительный", goal || "sell");

    if (brandVoice) {
      prompt += `\n\nВАЖНО — Голос бренда: ${brandVoice}\nПиши строго в этом стиле.`;
    }

    const message = await anthropic.messages.create({
      model: "claude-sonnet-4-6",
      max_tokens: 1500,
      messages: [{ role: "user", content: prompt }],
    });

    const raw = message.content[0].type === "text" ? message.content[0].text : "";

    let content = raw;
    let carouselData = null;

    if (type === "carousel") {
      try {
        const jsonMatch = raw.match(/\{[\s\S]*\}/);
        if (jsonMatch) carouselData = JSON.parse(jsonMatch[0]);
        content = carouselData?.cover ?? raw;
      } catch {
        content = raw;
      }
    }

    const viralAnalysis = type !== "carousel"
      ? await analyzeViralPotential(raw, platform)
      : null;
    const viralScore = viralAnalysis?.score ?? Math.floor(Math.random() * 12) + 85;

    try {
      const db = createServerClient();
      if (!db) throw new Error("not configured");
      await db.from("generated_content").insert({
        type, topic, platform,
        tone: tone || "Доверительный",
        content, viral_score: viralScore,
      });
    } catch (dbErr) {
      console.warn("Supabase не настроен:", dbErr);
    }

    return NextResponse.json({ content, viralScore, viralAnalysis, carouselData });
  } catch (err: any) {
    console.error("Generate error:", err);
    return NextResponse.json(
      { error: err.message || "Ошибка генерации" },
      { status: 500 }
    );
  }
}
